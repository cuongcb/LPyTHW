"""Rule for If-Statements

1. Every if-statement must have an else

2. If this else should never run because it doesn't make sense,
then you must use a die function in the else that prints out
an error message and dies, just like we did in the last exercise.

3. Never nest if-statements more than two deep and always try one deep

4. Treat if-statements like paragraphs, where each if-elif-else grouping
is like a set of sentences.

5. Your boolean tests should be simple

"""

"""Rules for Loops

1. Use a while-loop only to loop forever, and that means probably never

2. Use a for-loop for all other kinds of looping

"""

"""Tips for Debugging

1. Do not use a 'debugger'

2. The best way to debug a program is to use print

3. Code a little, run a little, fix a little

"""

